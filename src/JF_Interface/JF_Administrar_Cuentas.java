    private void jB_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_guardarActionPerformed

        String Operacion;

        if (jR_cuenta_crear.isSelected()) {
            Operacion = "crear";
        } else if (jR_cuenta_borrar.isSelected()) {
            Operacion = "borrar";
        } else {
            Operacion = "modificar";

        }

        String value
                = "1,"
                + Operacion  + ","
                + jT_cuenta_numero.getText() + ","
                + jT_cuenta_Propietario_nom.getText() + ","
                + jT_cuenta_Propietario_ape.getText() + ","
                + jT_cuenta_dinero.getText();

        Conectar_Sockets cs = new Conectar_Sockets();

        cs.Datos(value);
    }//GEN-LAST:event_jB_guardarActionPerformed

